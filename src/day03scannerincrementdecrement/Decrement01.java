package day03scannerincrementdecrement;

public class Decrement01 {
 
	public static void main(String[] args) {
		/* Dcrement is decrasing the value of number variable
		 * by "-" and "/"
		 * 1st way
		 */
	 	int num2=1;
	 	num2=num2-3;//-2
	 	System.out.println(num2);
	 	//2nd way
	 	num2 -= 4;//-6
	 	num2 /=3;//-2
	 	System.out.println(num2);
	 	
	 	//3rd way is only decrease by 1
	 	num2--;
	 	System.out.println(num2);
	 	
	 	
		
		
	}

}
